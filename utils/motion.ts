export function slideInFromLeft(delay: number) {
  return {
    hidden: { x: -100, opacity: 0 },
    visible: {
      x: 0,
      opacity: 1,
      transition: {
        delay: delay,
        duration: 0.5,
      },
    },
  };
}

export function sliderVariants(delay: number) {
  return {
    initial: {
      x: 0,
    },
    animate: {
      x: -4550,
      transition: {
        repeat: Infinity,
        repeatType: "mirror" as "mirror", // Specify the correct repeatType
        duration: 10,
      },
    },
  };
}

